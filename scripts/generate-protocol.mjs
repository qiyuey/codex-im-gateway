import { spawnSync } from "node:child_process";
import { rm, writeFile } from "node:fs/promises";

const outputDirectory = "src/codex/protocol";
const version = run("codex", ["--version"]).trim();

await rm(outputDirectory, { force: true, recursive: true });
run("codex", ["app-server", "generate-ts", "--out", outputDirectory], "inherit");
await writeFile(
  `${outputDirectory}/GENERATED.md`,
  `# Generated app-server protocol\n\nGenerated by \`${version}\`. Do not edit these TypeScript files by hand.\n`,
);

process.stdout.write(`Generated app-server protocol with ${version}.\n`);

function run(command, args, stdio = "pipe") {
  const result = spawnSync(command, args, { encoding: "utf8", stdio });
  if (result.status !== 0) throw new Error(`${command} ${args.join(" ")} failed`);
  return result.stdout ?? "";
}
